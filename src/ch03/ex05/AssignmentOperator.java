package ch03.ex05;

public class AssignmentOperator 
{

	public static void main(String[] args) 
	{
		int val1 = 3;
		val1+=3; //val1 = val1+3;
		System.out.println(val1);
		
		int val2 = 3;
		val2-=4; //val2 = val2-4;
		System.out.println(val2);
		
		int val3 = 3;
		val3*=4; //val3 = val3*4;
		System.out.println(val3);
		
		int val4 = 3;
		val4/=4; //val4 = val4/4;
		System.out.println(val4);

	}

}
