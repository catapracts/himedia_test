package ch03.ex02;

public class BitWiseOperator2 {

	public static void main(String[] args) 
	{
		System.out.println(3 & 10); //0011 & 1010 = 0010
		System.out.println(3 | 10); //0011 | 1010 = 1011
		System.out.println(3 ^ 10); //0011 ^ 1010 = 1001
		System.out.println(~3); //~0011 = 1/100 = -4
		System.out.println(~10); //~1010 = 0101 = -11

	}

}
